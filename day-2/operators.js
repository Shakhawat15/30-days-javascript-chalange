// Day 2: Operators

// Activity 1: Arithmetic Operators

// Task 1: Write a program to add two numbers and log the result to the console.
const num1 = 10;
const num2 = 20;
const sum = num1 + num2;
console.log("Task-1: ", sum);

// Task 2: Write a program to subtract two numbers and log the result to the console.
const num3 = 20;
const num4 = 10;
const difference = num3 - num4;
console.log("Task-2: ", difference);

// Task 3: Write a program to multiply two numbers and log the result to the console.
const num5 = 10;
const num6 = 20;
const product = num5 * num6;
console.log("Task-3: ", product);

// Task 4: Write a program to divide two numbers and log the result to the console.
const num7 = 20;
const num8 = 10;
const quotient = num7 / num8;
console.log("Task-4: ", quotient);

// Task 5: Write a program to find the remainder when two numbers are divided and log the result to the console.
const num9 = 20;
const num10 = 10;
const remainder = num9 % num10;
console.log("Task-5: ", remainder);

// Activity 2: Assignment Operators

// Task 6: Use the += operator to add a number to a variable and log the result to the console.
let num11 = 10;
num11 += 5;
console.log("Task-6: ", num11);

// Task 7: Use the -= operator to subtract a number from a variable and log the result to the console.
let num12 = 20;
num12 -= 5;
console.log("Task-7: ", num12);

// Activity 3: Comparison Operators

// Task 8: Write a program to compare two numbers using > and < and log the result to the console.
const num13 = 10;
const num14 = 20;
console.log("Task-8: ", num13 > num14);
console.log("Task-8: ", num13 < num14);

// Task 9: Write a program to compare two numbers using >= and <= and log the result to the console.
const num15 = 10;
const num16 = 20;
console.log("Task-9: ", num15 >= num16);
console.log("Task-9: ", num15 <= num16);

// Task 10: Write a program to compare two numbers using == and === and log the result to the console.
const num17 = 10;
const num18 = "10";
console.log("Task-10: ", num17 == num18);
console.log("Task-10: ", num17 === num18);

// Activity 4: Logical Operators

// Task 11: Write a program that uses the && operator to combine two conditions and log the result to the console.
const num19 = 10;
const num20 = 20;
console.log("Task-11: ", num19 < num20 && num19 > num20);

// Task 12: Write a program that uses the || operator to combine two conditions and log the result to the console.
const num21 = 10;
const num22 = 20;
console.log("Task-12: ", num21 < num22 || num21 > num22);

// Task 13: Write a program that uses the ! operator to negate a condition and log the result to the console.
const num23 = 10;
const num24 = 20;
console.log("Task-13: ", !(num23 < num24));

// Activity 5: Ternary Operator

// Task 14: Write a program that uses the ternary operator to check if a number is positive or negative and log the result to the console.
const num25 = 10;
const result = num25 > 0 ? "Positive" : "Negative";
console.log("Task-14: ", result);

